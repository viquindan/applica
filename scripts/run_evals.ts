import fs from 'fs';
import path from 'path';
import { tailorCV } from '../src/core/tailoring/cvTailor';
import { generateObject } from 'ai';
import { google } from '@ai-sdk/google';
import { z } from 'zod';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const DATASET_PATH = path.join(__dirname, '../evals/datasets/tailor_test_cases.json');

const judgeSchema = z.object({
  pass: z.boolean().describe("Whether Claude met the expected criteria fully and truthfully."),
  score: z.number().describe("Quality score from 0 to 100."),
  reasoning: z.string().describe("Detailed reasoning for why it passed or failed, referencing specific text."),
});

async function runEvals() {
  console.log('\n--- Starting LLMOps Evaluation Run ---');

  if (!process.env.ANTHROPIC_API_KEY || !process.env.GOOGLE_GENERATIVE_AI_API_KEY) {
    console.error(' ERROR: Missing API keys. Make sure ANTHROPIC_API_KEY and GOOGLE_GENERATIVE_AI_API_KEY are set.');
    process.exit(1);
  }

  const dataset = JSON.parse(fs.readFileSync(DATASET_PATH, 'utf-8'));
  let passedCount = 0;

  for (const tc of dataset) {
    console.log(`\n Running Test: [${tc.id}] ${tc.description}`);

    // 1. Run Claude (The Worker)
    console.log(' -> Generating CV with Claude (Anthropic)...');
    let claudeResult;
    try {
      claudeResult = await tailorCV(
        tc.input.cvText,
        tc.input.vacancy,
        tc.input.level,
        tc.input.tone,
        { provider: 'anthropic', apiKey: process.env.ANTHROPIC_API_KEY, model: 'claude-3-5-sonnet-20241022' }
      );
    } catch (e: any) {
       console.error(` Failed to call Claude: ${e.message}`);
       continue;
    }

    // 2. Run Gemini (The Judge)
    console.log(' -> Evaluating response with Gemini (Google)...');
    const prompt = `
      You are an AI auditor. Evaluate the output generated by Claude against the original instructions and expected criteria.

      [ORIGINAL CV]:
      ${tc.input.cvText}

      [VACANCY]:
      ${JSON.stringify(tc.input.vacancy)}

      [EXPECTED CRITERIA]:
      ${tc.expectedCriteria}

      [CLAUDE'S OUTPUT TO EVALUATE]:
      ${JSON.stringify(claudeResult)}

      Assess if Claude respected the original CV facts (no hallucinations) and met the criteria.
    `;

    try {
      const evaluation = await generateObject({
        model: google('gemini-2.5-flash'),
        schema: judgeSchema,
        prompt,
      });

      const { pass, score, reasoning } = evaluation.object;

      if (pass) {
        passedCount++;
        console.log(` PASS (Score: ${score}/100)`);
        console.log(` Reasoning: ${reasoning}`);
      } else {
        console.log(` FAIL (Score: ${score}/100)`);
        console.log(` Reasoning: ${reasoning}`);
      }
    } catch (e: any) {
      console.error(` Failed to call Gemini Judge: ${e.message}`);
    }
  }

  const passRate = (passedCount / dataset.length) * 100;
  console.log(`\n--- Eval Run Complete ---`);
  console.log(`Total: ${dataset.length} | Passed: ${passedCount} | Pass Rate: ${passRate.toFixed(1)}%\n`);
}

runEvals();
